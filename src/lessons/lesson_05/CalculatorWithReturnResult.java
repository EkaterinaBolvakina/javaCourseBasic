package lessons.lesson_05;

public class CalculatorWithReturnResult {

    public int sum(int a, int b){
        int result = a + b;
        return result;
    }

    public int subtraction(int a, int b){
        int result = a - b;
    return result;
}
    public int multiplication(int a, int b){
        int result = a * b;
        return result;
    }
    public int division(int a, int b){
        int result = a / b;
        return result;
    }

}
